# print a hard-coded string
print("Hello world")

# print a hard-coded string with a variable
name = "Tom Jones"
print("Hello %s" % name)

# print with multiple variables
color1 = "blue"
color2 = "green"
print("the best colors are %s and %s" % (color1, color2))

# print with a numeric (integer) variable
best_number = 42
print("the best number is %d" % best_number)

# print with a float variable
value_of_pi = 3.14
print("value of pi is %f" % value_of_pi)

