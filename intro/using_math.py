# first, we'll look at integers
print("simple math with integers")

# addition
x = 2
y = 3
z = x + 2
print("z = %d (addition)" % z)

# subtraction
x = 9
y = 2
z = x - y
print("z = %d (subtraction)" % z)

# multiplication
x = 3
y = 8
z = x * y
print("z = %d (multiplication)" % z)

# division
x = 8
y = 2
z = x / y
print("z = %d (division)" % z)

# modulo (remainder after division)
x = 7
y = 5
z = x % y
print("z = %d (modulo)" % z)

